// 子订单状态汇总 - 返回数组格式 [{status, count}] 供 ChatBubble map 渲染
export function aggregateSubOrderStatus(order) {
  const subs = order.subOrders || [];
  const statusCount = {};
  subs.forEach(s => {
    statusCount[s.status] = (statusCount[s.status] || 0) + 1;
  });
  return Object.entries(statusCount).map(([status, count]) => ({ status, count }));
}

// 商品汇总（用于欢迎卡片）
export function getProductSummary(storeId) {
  const store = mockStores.find(s => s.storeId === storeId);
  if (!store) return '';
  
  const allProducts = store.orders.flatMap(o => o.subOrders.flatMap(s => s.products));
  const uniqueProducts = [...new Set(allProducts.map(p => p.name))];
  
  if (uniqueProducts.length <= 3) {
    return uniqueProducts.join('、');
  }
  return uniqueProducts.slice(0, 3).join('、') + `等${uniqueProducts.length}种商品`;
}
